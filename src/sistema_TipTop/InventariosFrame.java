    private void cargarColumnasTabla() {
        modeloTabla.addColumn("Clave");
        modeloTabla.addColumn("Nombre");
        modeloTabla.addColumn("Unidad");
        modeloTabla.addColumn("Precio Compra");
        modeloTabla.addColumn("Precio Venta");
        modeloTabla.addColumn("Existencias");
    }

    private void cargarModeloTabla() {
        ArrayList<articulo> listaArticulos = bd.obtenerArticulos();
        int numArticulos = listaArticulos.size();
        modeloTabla.setNumRows(numArticulos);

        for (int i = 0; i < numArticulos; i++) {
            articulo art = listaArticulos.get(i);
            int clave = art.getIdArticulo();
            String nombre = art.getNombreArt();
            Double unidad = art.getuMedidaArticulo();
            Double pCompra = art.getpCompraArticulo();
            Double pVenta = art.getpVentaArticulo();
            int existencias = art.getExistenciasArticulo();

            modeloTabla.setValueAt(art, i, 0);
            modeloTabla.setValueAt(nombre, i, 1);
            modeloTabla.setValueAt(unidad, i, 2);
            modeloTabla.setValueAt(pCompra, i, 3);
            modeloTabla.setValueAt(pVenta, i, 4);
            modeloTabla.setValueAt(existencias, i, 5);
        }
    }

