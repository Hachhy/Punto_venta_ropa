    private void btnInsertarExistenciasActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnInsertarExistenciasActionPerformed
        int existenciasAgregar = Integer.parseInt(txfIngresarExistencias.getText());
        int existenciasActual = articuloSeleccionado.getExistenciasArticulo();
        int nuevaExistencia = existenciasAgregar + existenciasActual;
        bd.actualizarInventario(articuloSeleccionado, nuevaExistencia);
        cargarModeloTabla();
    }//GEN-LAST:event_btnInsertarExistenciasActionPerformed

