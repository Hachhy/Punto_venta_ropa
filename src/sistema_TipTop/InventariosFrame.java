    private void btnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarActionPerformed
        String nombreArt= articuloSeleccionado.getNombreArt();
        
        ImageIcon fotoArt=null;
        areaArticulos frameArt=null;
        
        int opcion=JOptionPane.showConfirmDialog(this, "¿Seguro que deseas modificar el articulo "+nombreArt+" ?");
        
        if(opcion==0){
            try {
                InputStream is=bd.buscarFoto(articuloSeleccionado);
                BufferedImage bi=ImageIO.read(is);
                fotoArt=new ImageIcon(bi);
                frameArt=new areaArticulos(null, true,articuloSeleccionado,fotoArt,"",true);
                frameArt.setVisible(true);
                
                if(frameArt!=null){
                    if(frameArt.getInformacion()!=""){
                        cargarModeloTabla();
                    }
                }
                
            } catch (IOException e) {
                e.printStackTrace(System.err);
            }
        }
    }//GEN-LAST:event_btnModificarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
      int opcion=JOptionPane.showConfirmDialog(this, "¿Seguro que deseas eliminar el articulo?");
      
      if(opcion==0){
          modeloTabla.removeRow(tbInventario.getSelectedRow());
          bd.borrarArticulo(articuloSeleccionado);
      }
    }//GEN-LAST:event_btnEliminarActionPerformed

    private void limpiarTabla() {
        int numFilas = modeloTabla.getRowCount();
        if (numFilas > 0) {
            for (int i = numFilas - 1; i >= 0; i--) {
                modeloTabla.removeRow(i);
            }
        }
    }


