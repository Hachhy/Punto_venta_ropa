    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed

        String nombre = txfNombre.getText();
        int clave = Integer.parseInt(txfClave.getText());
        String descripcion = txfDescripcion.getText();
        int stock = Integer.parseInt(txfStock.getText());
        double uMedida = Double.parseDouble(txfUnidMedida.getText());
        double pCompra = Double.parseDouble(txfPrecCompra.getText());
        double pVenta = Double.parseDouble(txfPrecVenta.getText());
        categorias cat = (categorias) cbxCategoria.getSelectedItem();
        proveedor prov = (proveedor) cbxProveedor.getSelectedItem();
        int existencias = Integer.parseInt(txfExistencias.getText());
        
        ImageIcon foto=(ImageIcon) lblFoto.getIcon();
        
        if (estaActualizando) {
            if (fotoArt == null) {
                JOptionPane.showMessageDialog(this, "Inserte una imagen del producto11", "Imagen del producto", JOptionPane.WARNING_MESSAGE);

            } else {
                articulo art = new articulo(clave, nombre, descripcion, stock, uMedida, pVenta, pCompra, cat.getIdCategoria(), prov.getIdProveedor(), existencias, fotoArt);
                bd.actualizarArticulo(art);
                JOptionPane.showMessageDialog(this, "Se ha actualizado el articulo");
                this.dispose();
                this.informacion = "1";
            }

        } else {
            if (fotoArt == null) {
                JOptionPane.showMessageDialog(this, "Inserte una imagen del producto", "Imagen del producto", JOptionPane.WARNING_MESSAGE);
            } else {
                articulo art = new articulo(clave, nombre, descripcion, stock, uMedida, pVenta, pCompra, cat.getIdCategoria(), prov.getIdProveedor(), existencias, fotoArt);
                bd.insertarArticulo(art);
                JOptionPane.showMessageDialog(null, "Los datos se han guardado correctamente");
                this.dispose();
            }
        }
    }//GEN-LAST:event_btnAgregarActionPerformed

