    private void btnExaminarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnExaminarMouseClicked
        JFileChooser chooser = new JFileChooser();
        FileNameExtensionFilter filter = new FileNameExtensionFilter("Formatos permitidos jpg,jpge,gif o png", "jpg", "png", "gif");
        chooser.setFileFilter(filter);

        int returnval = chooser.showOpenDialog(this);
        if (returnval == JFileChooser.APPROVE_OPTION) {
            int anchoFoto = lblFoto.getWidth();
            int altoFoto = lblFoto.getHeight();
            fotoArt = chooser.getSelectedFile();
            ImageIcon icono = new ImageIcon(fotoArt.getAbsolutePath());
            Image imagen = icono.getImage();
            Image imagenRedimensionado = imagen.getScaledInstance(anchoFoto, altoFoto, Image.SCALE_DEFAULT);
            ImageIcon imagenRedimensionada = new ImageIcon(imagenRedimensionado);
            lblFoto.setIcon(imagenRedimensionada);

        }
    }//GEN-LAST:event_btnExaminarMouseClicked

    private void cargarModeloCategorias() {
        ArrayList<categorias> listaCategorias;
        listaCategorias = bd.obtenerCategorias();

        for (categorias categoria : listaCategorias) {
            modeloCategorias.addElement(categoria);
        }
    }

    private void cargarModeloProveedor() {
        ArrayList<proveedor> listaProveedor;
        listaProveedor = bd.obtenerProveedores();

        for (proveedor prov : listaProveedor) {
            modeloProveedor.addElement(prov);
        }
    }

    public void cargarArticulo(articulo art, ImageIcon icon) {

        this.fotoArt = art.getFotoArticulo();

        lblFoto.setIcon(icon);
        String nombre = art.getNombreArt();
        int clave = art.getIdArticulo();
        String descripcion = art.getDescArticulo();
        int stock = art.getStockArticulo();
        double uMedida = art.getuMedidaArticulo();
        double pCompra = art.getpCompraArticulo();
        double pVenta = art.getpVentaArticulo();
        int existencias = art.getExistenciasArticulo();

        txfNombre.setText(nombre);
        txfClave.setText(String.valueOf(clave));
        txfDescripcion.setText(descripcion);
        txfStock.setText(String.valueOf(stock));
        txfUnidMedida.setText(String.valueOf(uMedida));
        txfPrecCompra.setText(String.valueOf(pCompra));
        txfPrecVenta.setText(String.valueOf(pVenta));
        txfExistencias.setText(String.valueOf(existencias));

        txfClave.setEnabled(false);
        txfNombre.setEnabled(false);

    }

